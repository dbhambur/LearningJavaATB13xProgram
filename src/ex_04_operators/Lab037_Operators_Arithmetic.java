package ex_04_operators;

public class Lab037_Operators_Arithmetic {
    public static void main(String[] args) {
        //Arithmetic operator
        //   + (Addition)
        //   - (Substraction)
        //   * (Multiplication)
        //   / (Division)
        //   % (Modulas) / Modulas - Remainder

        int a = 30;
        int b = 27;
        float c =3.2f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c);
        System.out.println("a+b"); // Output will be a+b because value is in string format i.e. under double quote



    }
}
