package ex_10_For_Loop;

public class Lab107_for_loop2 {
    public static void main(String[] args) {

        for(long i=1l;i<15;i++){
            System.out.println(i);
        }
        for(float f =0.0f;f<10.7;f++){
            System.out.println(f);  // Note : we can use long and float for "for loop" but it is not advisable
        }
    }
}
